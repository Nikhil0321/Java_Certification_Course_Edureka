public class Arithmatic_operations 
{
    public static void main(String args[])
    {
        int a,b;
        a=59;
        b=23;
        int sum=a+b;
        int diff=a-b;
        int mul=a*b;
        int div=a/b;
        int mod=a%b;
        System.out.println("Sum of "+a+"+"+b+"="+sum);
        System.out.println("Sum of "+a+"-"+b+"="+diff);
        System.out.println("Sum of "+a+"*"+b+"="+mul);
        System.out.println("Sum of "+a+"/"+b+"="+div);
        System.out.println("Sum of "+a+"%"+b+"="+mod);
    }
}
